#!/usr/bin/env node
import { dirname, resolve } from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";
import { writeFile } from "node:fs/promises";
import openapiTS, { astToString } from "openapi-typescript";

const packageRoot = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const repoRoot = resolve(packageRoot, "../..");
const outFile = resolve(packageRoot, "src/acp/types.generated.ts");

const specs = [
  {
    namespace: "AcpCheckoutOpenApi",
    path: "protocols/acp/spec/2026-04-17/openapi/openapi.agentic_checkout.yaml"
  },
  {
    namespace: "AcpDelegatePaymentOpenApi",
    path: "protocols/acp/spec/2026-04-17/openapi/openapi.delegate_payment.yaml"
  },
  {
    namespace: "AcpWebhookOpenApi",
    path: "protocols/acp/spec/2026-04-17/openapi/openapi.agentic_checkout_webhook.yaml"
  },
  {
    namespace: "AcpFeedOpenApi",
    path: "protocols/acp/spec/2026-04-17/openapi/openapi.feed.yaml"
  }
];

const blocks = [];
for (const spec of specs) {
  const input = pathToFileURL(resolve(repoRoot, spec.path));
  const ast = await openapiTS(input, { cwd: repoRoot, silent: true });
  blocks.push(namespaceBlock(spec.namespace, astToString(ast)));
}

const content = `${header()}${blocks.join("\n\n")}\n\n${aliases()}\n`;
await writeFile(outFile, content, "utf8");

function header() {
  return [
    "/**",
    " * This file was auto-generated by packages/protocol/scripts/generate-acp-types.mjs.",
    " * Do not make direct changes to the file.",
    " */",
    ""
  ].join("\n");
}

function namespaceBlock(name, generated) {
  const body = generated
    .replace(/^\/\*\*[\s\S]*?\*\/\n\n/, "")
    .trimEnd()
    .split("\n")
    .map((line) => (line ? `  ${line}` : ""))
    .join("\n");
  return `export declare namespace ${name} {\n${body}\n}`;
}

